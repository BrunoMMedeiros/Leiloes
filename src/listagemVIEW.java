    private void btnVenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVenderActionPerformed
        String id = id_vender_produto.getText();

        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite um ID vÃ¡lido", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int produtoID = Integer.parseInt(id);

        if (atualizarStatusProduto(produtoID)) {
            JOptionPane.showMessageDialog(this, "Produto vendido com sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao vender o produto", "Erro", JOptionPane.ERROR_MESSAGE);
        }

        listarProdutos();
    }//GEN-LAST:event_btnVenderActionPerformed

    private void btnVendasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVendasActionPerformed
        try (Connection conexao = DriverManager.getConnection("jdbc:mysql://localhost:3306/leilao", "root", "")) {
            String sql = "SELECT id, nome, valor, status FROM produtos";
            PreparedStatement statement = conexao.prepareStatement(sql);
            ResultSet resultSet = statement.executeQuery();

            DefaultTableModel model = (DefaultTableModel) listaProdutos.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                int id = resultSet.getInt("id");
                String nome = resultSet.getString("nome");
                double valor = resultSet.getDouble("valor");
                String status = resultSet.getString("status");

                model.addRow(new Object[]{id, nome, valor, status});
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao listar produtos: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnVendasActionPerformed

