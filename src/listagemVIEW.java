    private boolean atualizarStatusProduto(int id) {
    try (Connection conexao = DriverManager.getConnection("jdbc:mysql://localhost:3306/leilao", "root", "")) {
        String sql = "UPDATE produtos SET status = 'Vendido' WHERE id = ?";
        PreparedStatement statement = conexao.prepareStatement(sql);
        statement.setInt(1, id);
        int rowsUpdated = statement.executeUpdate();
        
        // Verifique se uma linha foi atualizada, indicando que o produto foi encontrado e atualizado
        return rowsUpdated > 0;
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Erro ao atualizar status: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        return false;
        }
    }
    
